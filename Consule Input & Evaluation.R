
#  "<-" is the assignment operator
x <- 5

# Both are ways to print 
print(x) # 5, explicit printing
x # 5, auto-printing

# prints [1] 5, indicating x is a vector and 
#               the first element is 5


# String 
msg <- "Hello"
msg # "Hello", auto-printing


# Integer Sequences 
y <- 1 : 20
y  # 1 - 20
